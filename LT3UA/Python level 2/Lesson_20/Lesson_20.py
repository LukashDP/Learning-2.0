def multiplication(num1, num2):
    result1 = num2 * num1
    return result1

def division(num1, num2):
    result2 = num1 / num2
    return result2

def area_of_a_rectangle(side1, side2):
    result3 = side1 * side2
    return result3

def kg_to_pounds(kg=100):
    return kg * 2.205

def pounds_to_kg(pd=100):
    return pd * 0.454